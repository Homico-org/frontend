// Helper functions
export function getPricesByCategory(category: PriceCategory): PriceItem[] {
  return priceDatabase.filter((item) => item.category === category);
}

export function searchPrices(query: string, lang: 'ka' | 'en' = 'ka'): PriceItem[] {
  const lowerQuery = query.toLowerCase();
  return priceDatabase.filter((item) => {
    const name = lang === 'ka' ? item.nameKa : item.nameEn;
    return name.toLowerCase().includes(lowerQuery) || item.workType.toLowerCase().includes(lowerQuery);
  });
}

export function getMarketAverage(itemId: string): number | null {
  const item = priceDatabase.find((p) => p.id === itemId);
  if (!item) return null;
  return item.priceMid;
}

export function getPriceStatus(
  price: number,
  itemId: string
): 'low' | 'normal' | 'high' | 'very_high' | 'unknown' {
  const item = priceDatabase.find((p) => p.id === itemId);
  if (!item) return 'unknown';

  const marketAvg = item.priceMid;
  const ratio = price / marketAvg;

  if (ratio <= 0.85) return 'low';
  if (ratio <= 1.2) return 'normal';
  if (ratio <= 1.4) return 'high';
  return 'very_high';
}
